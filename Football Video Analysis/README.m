# Football Video Analysis in MATLAB

## Overview
This project demonstrates video processing and analysis using MATLAB. The tasks involve working with a sample football video, applying computational techniques, and analyzing the output.

---

## Features
1. **Video Loading and Playback**:
   - Load and play the provided football video (`football_qcif.avi`).
2. **Frame-by-Frame Analysis**:
   - Analyze and process individual frames from the video.
3. **MATLAB Scripting**:
   - Implement video processing tasks efficiently using MATLAB's video functions.

---

## Files
- **`Readme.m`**: MATLAB script containing video analysis tasks.
- **`football_qcif.avi`**: Sample video file used for analysis.
- **`HW3_824783655_825764388.pdf`**: Original assignment instructions (optional for reference).

---

## Skills Demonstrated
- **Video Processing**:
  - Loading, playing, and analyzing video data in MATLAB.
  - Frame-level manipulation for detailed analysis.
- **MATLAB Programming**:
  - Use of MATLAB's video-related toolkits and functions.

---

## How to Use
1. Ensure MATLAB is installed on your system.
2. Place all files (`Readme.m` and `football_qcif.avi`) in the same directory.
3. Open `Readme.m` in MATLAB and run the script to execute tasks.

